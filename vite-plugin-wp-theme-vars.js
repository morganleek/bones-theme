import { readFileSync, writeFileSync } from 'fs';
import { resolve } from 'path';

/**
 * Converts camelCase keys to kebab-case to match how WordPress
 * generates CSS custom property names from theme.json custom values.
 */
function camelToKebab(str) {
    return str.replace(/([A-Z])/g, (match) => `-${match.toLowerCase()}`);
}

/**
 * Recursively flattens settings.custom into --wp--custom--* declarations.
 * Nested keys are joined with double dashes, camelCase converted to kebab-case.
 * e.g. { "heading-1": { "typography": { "fontSize": "4rem" } } }
 *   → { "--wp--custom--heading-1--typography--font-size": "4rem" }
 */
function flattenCustomVars(obj, prefix = '') {
    const vars = {};
    for (const [key, value] of Object.entries(obj)) {
        const kebabKey = camelToKebab(key);
        const fullKey = prefix ? `${prefix}--${kebabKey}` : kebabKey;
        if (value !== null && typeof value === 'object' && !Array.isArray(value)) {
            Object.assign(vars, flattenCustomVars(value, fullKey));
        } else {
            vars[`--wp--custom--${fullKey}`] = value;
        }
    }
    return vars;
}

function generateCss(root, themeJsonPath, outputPath) {
    const themeJson = JSON.parse(readFileSync(resolve(root, themeJsonPath), 'utf-8'));
    const s = themeJson.settings ?? {};
    const vars = {};

    // --wp--preset--color--{slug}
    for (const { slug, color } of s.color?.palette ?? []) {
        vars[`--wp--preset--color--${slug}`] = color;
    }

    // --wp--preset--gradient--{slug}
    for (const { slug, gradient } of s.color?.gradients ?? []) {
        vars[`--wp--preset--gradient--${slug}`] = gradient;
    }

    // --wp--preset--font-family--{slug}
    for (const { slug, fontFamily } of s.typography?.fontFamilies ?? []) {
        vars[`--wp--preset--font-family--${slug}`] = fontFamily;
    }

    // --wp--preset--font-size--{slug}
    for (const { slug, size } of s.typography?.fontSizes ?? []) {
        vars[`--wp--preset--font-size--${slug}`] = size;
    }

    // --wp--preset--spacing--{slug}
    for (const { slug, size } of s.spacing?.spacingSizes ?? []) {
        vars[`--wp--preset--spacing--${slug}`] = size;
    }

    // --wp--preset--aspect-ratio--{slug}
    for (const { slug, ratio } of s.dimensions?.aspectRatios ?? []) {
        vars[`--wp--preset--aspect-ratio--${slug}`] = ratio;
    }

    // --wp--custom--* (nested settings.custom object)
    if (s.custom) {
        Object.assign(vars, flattenCustomVars(s.custom));
    }

    const declarations = Object.entries(vars)
        .map(([prop, val]) => `    ${prop}: ${val};`)
        .join('\n');

    const css = [
        '/* Auto-generated by vite-plugin-wp-theme-vars.js — do not edit manually */',
        '/* Re-generated on each Vite start and whenever theme.json changes */',
        ':root {',
        declarations,
        '}',
        '',
    ].join('\n');

    writeFileSync(resolve(root, outputPath), css, 'utf-8');
    console.log(`[wp-theme-vars] ${Object.keys(vars).length} variables → ${outputPath}`);
}

/**
 * Vite plugin that reads theme.json and writes a CSS file containing every
 * --wp--preset--* and --wp--custom--* variable in a :root {} block.
 *
 * @param {object} [options]
 * @param {string} [options.themeJsonPath='theme.json']  Path relative to Vite root.
 * @param {string} [options.outputPath='src/styles/_wp-vars.css']  Path relative to Vite root.
 */
export function wpThemeVarsPlugin(options = {}) {
    const {
        themeJsonPath = 'theme.json',
        outputPath = 'src/styles/_wp-vars.css',
    } = options;

    let root;

    return {
        name: 'wp-theme-vars',
        configResolved(config) {
            root = config.root;
            generateCss(root, themeJsonPath, outputPath);
        },
        configureServer(server) {
            // Watch theme.json and regenerate on change during `vite dev`
            const abs = resolve(server.config.root, themeJsonPath);
            server.watcher.add(abs);
            server.watcher.on('change', (file) => {
                if (file === abs) {
                    generateCss(server.config.root, themeJsonPath, outputPath);
                }
            });
        },
    };
}
